package wazir;

public class shorthand {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        System.out.println("The sum of shorthand is: "+(a+=b));
        System.out.println("The subtruct of shorthand: "+(a-=b));
        System.out.println("The multiply of shorthand is: "+(a*=b));
        System.out.println("The divide of shorthand is: "+(a/=b));
        System.out.println("The mod of shorthand is: "+(a%=b));
    }
    
}
