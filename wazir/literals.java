package wazir;

public class literals {
    public static void main(String[] args) {
        int a = 10;
        float b = 10.1f;
        short c = 111;
        byte d = 2;
        long e = 101111l;
        double f = 10.1111d;
        char g = 'a';
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);
    }
    
}
