package wazir;

public class relationalop {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        System.out.println("The result of Greater than is: "+(a>b));
        System.out.println("The result of less than is: "+(a<b));
        System.out.println("The result of greater than and equal to is: "+(a>=b));
        System.out.println("The result of less than and equal to is: "+(a<=b));
        System.out.println("The result of equal to equal to is: "+(a==b));
        System.out.println("The result of not equal to is: "+(a!=b));
    }
    
}
